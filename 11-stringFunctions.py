# STRING FUNCTIONS ->

string = "Al Aqmar"

# 1) Ends With ->
str.endswith(string, "r")
# This will return True as the string ends with "r"

# 2) Starts With ->
str.startswith(string, "A")
# This will return True as the string starts with "A"

# 3) Count ->
str.count(string, "A")
# This will return 2 as the string has 2 "A"s

# 4) Find ->
str.find(string, "A")
# This will return 3 as the first "A" is at the 3rd index

# 5) Replace ->
str.replace(string, "A", "a")
# This will replace all the "A"s with "a"s

# 6) Lower ->
str.lower(string)
# This will convert the string to lowercase

# 7) Upper ->
str.upper(string)
# This will convert the string to uppercase

# 26) Capitalize ->
str.capitalize(string)
# This will capitalize the first letter of the string

# 8) Title ->
str.title(string)
# This will convert the string to title case

# 9) Split ->
str.split(string, " ")
# This will split the string into a list of strings at the spaces

# 10) Join ->
str.join(string, " ")
# This will join the strings in the list with the string as the separator

# 11) Strip ->
str.strip(string)
# This will remove the leading and trailing whitespaces from the string

# 12) Lstrip ->
str.lstrip(string)
# This will remove the leading whitespaces from the string

# 13) Rstrip ->
str.rstrip(string)
# This will remove the trailing whitespaces from the string

# 14) Isalnum ->
str.isalnum(string)
# This will return True if the string is alphanumeric

# 15) Isalpha ->
str.isalpha(string)
# This will return True if the string is alphabetic

# 16) Isdigit ->
str.isdigit(string)
# This will return True if the string is numeric

# 17) Islower ->
str.islower(string)
# This will return True if the string is in lowercase

# 18) Isupper ->
str.isupper(string)
# This will return True if the string is in uppercase

# 19) Istitle ->
str.istitle(string)
# This will return True if the string is in title case

# 20) Isnumeric ->
str.isnumeric(string)
# This will return True if the string is numeric

# 21) Isprintable ->
str.isprintable(string)
# This will return True if the string is printable

# 22) Isspace ->
str.isspace(string)
# This will return True if the string is a whitespace

# 23) Isidentifier ->
str.isidentifier(string)
# This will return True if the string is an identifier

# 24) Isascii ->
str.isascii(string)
# This will return True if the string is ASCII

# 25) Isdecimal ->
str.isdecimal(string)
# This will return True if the string is a decimal


