# input() statement is used to take input from the user in Python.

# Name Input ->
name = input("Enter your name: ")

# Age Input ->
age = input("Enter your age: ")


# Input functions stores the input in the form of a string by default.
# To convert the input into an integer we use the int() function.
# int() defines the type of the input as an integer.


# Age Input as Integer ->
_age = int(input("Enter your age (Integer Format): "))

# Similar to the int() function, we can also use the float() function to convert the input into a float value.


# Lets print the values of the variables.
print("Name: ", name)
print("Age: ", age)
print("Age (Integer Format): ", _age)
